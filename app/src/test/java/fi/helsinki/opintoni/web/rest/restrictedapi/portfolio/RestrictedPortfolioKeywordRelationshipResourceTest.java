package fi.helsinki.opintoni.web.rest.restrictedapi.portfolio;

import org.junit.Test;

import static fi.helsinki.opintoni.security.SecurityRequestPostProcessors.securityContext;
import static fi.helsinki.opintoni.security.TestSecurityContext.studentSecurityContext;
import static org.hamcrest.collection.IsCollectionWithSize.hasSize;
import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.get;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.jsonPath;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.status;

public class RestrictedPortfolioKeywordRelationshipResourceTest extends RestrictedPortfolioTest {

    @Test
    public void thatKeywordsAreReturned() throws Exception {
        mockMvc.perform(get(RESTRICTED_PORTFOLIO_URL + "/keyword")
            .with(securityContext(studentSecurityContext())))
            .andExpect(status().isOk())
            .andExpect(jsonPath("$").isArray())
            .andExpect(jsonPath("$").value(hasSize(1)))
            .andExpect(jsonPath("$[0].title").value("Keyword 1"));
    }
}
