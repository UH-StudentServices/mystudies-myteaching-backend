package fi.helsinki.opintoni.web.rest.restrictedapi.portfolio;

import org.junit.Test;
import org.springframework.http.MediaType;

import static fi.helsinki.opintoni.security.SecurityRequestPostProcessors.securityContext;
import static fi.helsinki.opintoni.security.TestSecurityContext.studentSecurityContext;
import static org.hamcrest.Matchers.hasSize;
import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.get;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.jsonPath;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.status;

public class RestrictedPortfolioFavoriteResourceTest extends RestrictedPortfolioTest {

    @Test
    public void thatPortfolioFavoritesAreReturned() throws Exception {
        mockMvc.perform(get(RESTRICTED_PORTFOLIO_URL + "/favorites")
            .with(securityContext(studentSecurityContext()))
            .characterEncoding("UTF-8")
            .accept(MediaType.APPLICATION_JSON))
            .andExpect(status().isOk())
            .andExpect(jsonPath("$").isArray())
            .andExpect(jsonPath("$", hasSize(2)))
            .andExpect(jsonPath("$[0].id").value(6))
            .andExpect(jsonPath("$[1].id").value(8));
    }

}
